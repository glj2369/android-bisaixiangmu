package com.example.jun.bisaixiangmu.bean;

public class Bean45 {
    private int background;
    private String carName;
    private String carText;
    private int yuE;

    public Bean45(int background, String carName, String carText, int yuE) {
        this.background = background;
        this.carName = carName;
        this.carText = carText;
        this.yuE = yuE;
    }

    public int getBackground() {
        return background;
    }

    public void setBackground(int background) {
        this.background = background;
    }

    public String getCarName() {
        return carName;
    }

    public void setCarName(String carName) {
        this.carName = carName;
    }

    public String getCarText() {
        return carText;
    }

    public void setCarText(String carText) {
        this.carText = carText;
    }

    public int getYuE() {
        return yuE;
    }

    public void setYuE(int yuE) {
        this.yuE = yuE;
    }
}
